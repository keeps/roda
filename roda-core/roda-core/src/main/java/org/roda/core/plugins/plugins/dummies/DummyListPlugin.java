public class DummyListPlugin<T extends IsRODAObject> extends AbstractPlugin<T> {

  @Override
  public void init() throws PluginException {
    // do nothing
  }

  @Override
  public void shutdown() {
    // do nothing
  }

  @Override
  public String getName() {
    return "Dummy list";
  }

  @Override
  public String getDescription() {
    return "Dummy to test list of lites";
  }

  @Override
  public String getVersionImpl() {
    return "1.0";
  }

  @Override
  public Report execute(IndexService index, ModelService model, StorageService storage,
    List<LiteOptionalWithCause> liteList) throws PluginException {
    return PluginHelper.processObjects(this, new RODAObjectProcessingLogic<T>() {
      @Override
      public void process(IndexService index, ModelService model, StorageService storage, Report report, Job cachedJob,
        SimpleJobPluginInfo jobPluginInfo, Plugin<T> plugin, T object) {
        jobPluginInfo.incrementObjectsProcessedWithSuccess();
      }
    }, index, model, storage, liteList);
  }

  @Override
  public Plugin<T> cloneMe() {
    return new DummyListPlugin<T>();
  }

  @Override
  public PluginType getType() {
    return PluginType.AIP_TO_AIP;
  }

  @Override
  public boolean areParameterValuesValid() {
    return true;
  }

  // TODO FIX
  @Override
  public PreservationEventType getPreservationEventType() {
    return null;
  }

  @Override
  public String getPreservationEventDescription() {
    return null;
  }

  @Override
  public String getPreservationEventSuccessMessage() {
    return null;
  }

  @Override
  public String getPreservationEventFailureMessage() {
    return null;
  }

  @Override
  public Report beforeAllExecute(IndexService index, ModelService model, StorageService storage)
    throws PluginException {
    // do nothing
    return null;
  }

  @Override
  public Report afterAllExecute(IndexService index, ModelService model, StorageService storage) throws PluginException {
    // do nothing
    return null;
  }

  @Override
  public List<String> getCategories() {
    return Arrays.asList(RodaConstants.PLUGIN_CATEGORY_NOT_LISTABLE);
  }

  @SuppressWarnings({"unchecked", "rawtypes"})
  @Override
  public List<Class<T>> getObjectClasses() {
    List<Class<? extends IsRODAObject>> list = new ArrayList<>();
    list.add(AIP.class);
    list.add(Representation.class);
    list.add(File.class);
    list.add(Job.class);
    list.add(Report.class);
    list.add(Notification.class);
    list.add(Risk.class);
    list.add(Format.class);
    list.add(RiskIncidence.class);
    list.add(RODAMember.class);
    // list.add(LogEntry.class);
    list.add(PreservationMetadata.class);
    list.add(DescriptiveMetadata.class);
    list.add(DIP.class);
    list.add(DIPFile.class);
    list.add(TransferredResource.class);
    return (List) list;
  }
}
